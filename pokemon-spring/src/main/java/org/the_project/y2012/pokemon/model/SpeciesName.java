package org.the_project.y2012.pokemon.model;

public class SpeciesName {
    String species;
    String name;

    public SpeciesName(String species, String name) {
        super();
        this.species = species;
        this.name = name;
    }

    public String getSpecies() {
        return species;
    }

    public String getName() {
        return name;
    }
}
