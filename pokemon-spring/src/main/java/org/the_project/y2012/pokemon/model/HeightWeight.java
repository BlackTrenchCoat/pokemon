package org.the_project.y2012.pokemon.model;

public class HeightWeight {

    Integer height;
    Integer weight;

    public HeightWeight(Integer height, Integer weight) {
        super();
        this.height = height;
        this.weight = weight;
    }

    public Integer getHeight() {
        return height;
    }

    public Integer getWeight() {
        return weight;
    }
    
}
